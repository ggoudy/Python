# Floor division uses // 
# Truncates the decimal without rounding
# Returns an integer result
print("The floor division of 15 by 9 is", 15//9)

# Power uses **
# ** operator squares the provided integer
print("2 sqaured by 2 is", 2**2)

# Modulo uses %
#  % operator returns the remainder after division
print("The remainder of 5 divided by 2 is", 5%2)